#include<iostream>
using namespace std;

int main(){
	int a,b,add,sub,mul,div,mod;
	
	cin>>a>>b;
	
	add = a+b;
	sub = a-b;
	mul = a*b;
	div = a/b;
	mod = a%b;
	
	cout<<"Sum: "<<add<<endl;
	cout<<"Sub: "<<sub<<endl;
	cout<<"Mul: "<<mul<<endl;
	cout<<"Div: "<<div<<endl;
	cout<<"Mod: "<<mod<<endl;
	
}
